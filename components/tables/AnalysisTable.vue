<template>
  <table-wrapper
    v-bind="{ showSearch }"
    :headers="useDynamicHeaders ? dynamicHeaders : filteredHeaders"
    :items="items"
    :options="options"
    :count="count"
    v-on="$listeners"
  >
    <template #item.id="{ item }">
      <nuxt-link
        class="text-link"
        :to="localePath({ name: 'analysis-id', params: { id: item.id } })"
      >
        {{ item.id }}
      </nuxt-link>
    </template>
    <template #item.sample_number="{ item }">
      <nuxt-link
        v-if="item.sample_id"
        class="text-link"
        :to="localePath({ name: 'sample-id', params: { id: item.sample_id } })"
      >
        {{ item.sample_number }}
      </nuxt-link>
    </template>
    <template #item.locality="{ item }">
      <nuxt-link
        v-if="item.locality_id"
        class="text-link"
        :to="
          localePath({ name: 'locality-id', params: { id: item.locality_id } })
        "
      >
        {{ $translate({ et: item.locality, en: item.locality_en }) }}
      </nuxt-link>
      <nuxt-link
        v-else-if="item.site_id"
        class="text-link"
        :to="localePath({ name: 'site-id', params: { id: item.site_id } })"
      >
        {{ $translate({ et: item.name, en: item.name_en }) }}
      </nuxt-link>
    </template>
    <template #item.thickness="{ item }">
      {{
        !item.depth_interval || !item.depth
          ? null
          : round(item.depth_interval - item.depth, 3)
      }}
    </template>
    <template #item.method="{ item }">
      {{
        $translate({
          et: item.analysis_method,
          en: item.analysis_method_en,
        })
      }}
    </template>
    <template #item.method_details="{ item }">
      {{
        $translate({
          et: item.method_details,
          en: item.method_details_en,
        })
      }}
    </template>
    <template #item.date="{ item }">
      <div v-if="item.date">
        {{ item.date ? $formatDate(item.date) : null }}
      </div>
      <div v-else>{{ item.date_free }}</div>
    </template>
  </table-wrapper>
</template>

<script>
import { round } from 'lodash'
import { mapState } from 'vuex'
import TableWrapper from '~/components/tables/TableWrapper.vue'
export default {
  name: 'AnalysisTable',
  components: { TableWrapper },
  props: {
    showSearch: {
      type: Boolean,
      default: true,
    },
    items: {
      type: Array,
      default: () => [],
    },
    count: {
      type: Number,
      default: 0,
    },
    options: {
      type: Object,
      default: () => ({
        page: 1,
        itemsPerPage: 25,
        sortBy: [],
        sortDesc: [],
      }),
    },
    useDynamicHeaders: {
      type: Boolean,
      default: false,
    },
    hideDepth: Boolean,
    hideLocality: Boolean,
    hideSample: Boolean,
  },
  data() {
    return {
      headers: [
        { text: this.$t('analysis.id'), value: 'id' },
        { text: this.$t('analysis.sampleNumber'), value: 'sample_number' },
        { text: this.$t('analysis.locality'), value: 'locality' },
        { text: this.$t('analysis.depth'), value: 'depth' },
        { text: this.$t('analysis.depthInterval'), value: 'depth_interval' },

        { text: this.$t('analysis.method'), value: 'method' },
        {
          text: this.$t('analysis.methodDetails'),
          value: 'method_details',
        },
        { text: this.$t('analysis.analysedBy'), value: 'agent' },
        { text: this.$t('analysis.date'), value: 'date' },
      ],
    }
  },
  computed: {
    ...mapState('table_headers', {
      tableHeaders(state) {
        return state.analysis.tableHeaders
      },
    }),

    filteredHeaders() {
      return this[this.useDynamicHeaders ? 'tableHeaders' : 'headers'].filter(
        (item) => {
          if (item.value.includes('depth')) return !this.hideDepth
          else if (item.value === 'locality') return !this.hideLocality
          else if (item.value === 'sample_number') return !this.hideSample
          else return item
        }
      )
    },

    dynamicHeaders() {
      return this.filteredHeaders.reduce((prev, item) => {
        if (item.show) {
          prev.push({ ...item, text: this.$t(item.text) })
        }
        return prev
      }, [])
    },
  },
  methods: {
    round,
  },
}
</script>

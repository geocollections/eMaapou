<template>
  <search>
    <template #title>
      <title-card
        :title="$t('common.analyticalDataCount')"
        icon="mdi-chart-line"
        class="title-analysis"
      />
    </template>

    <template #form>
      <analytical-data-search-form />
    </template>

    <template #result>
      <div class="py-1 pl-2 text-h6">
        {{ count ? $tc('common.count', count) : '&nbsp;' }}
      </div>
      <analytical-data-table
        :show-search="false"
        :items="items"
        :count="count"
        :options="options"
        dynamic-headers
        @update="handleUpdate"
      />
    </template>
  </search>
</template>

<script>
import { mapState, mapActions } from 'vuex'
import AnalyticalDataTable from '~/components/tables/AnalyticalDataTable.vue'
import AnalyticalDataSearchForm from '~/components/search/forms/AnalyticalDataSearchForm.vue'
import Search from '~/components/templates/Search'
import TitleCard from '~/components/TitleCard.vue'

export default {
  name: 'AnalysisSearch',
  components: {
    Search,
    AnalyticalDataSearchForm,
    AnalyticalDataTable,
    TitleCard,
  },
  head() {
    return {
      title: this.$t('analyticalData.pageTitle'),
      meta: [
        {
          property: 'og:title',
          content: this.$t('analyticalData.pageTitle'),
          hid: 'og:title',
        },
      ],
    }
  },
  computed: {
    ...mapState('search/analytical_data', ['items', 'count', 'options']),
  },
  methods: {
    ...mapActions('search/analytical_data', ['searchAnalyticalData']),
    handleUpdate(tableState) {
      this.searchAnalyticalData(tableState?.options)
    },
  },
}
</script>

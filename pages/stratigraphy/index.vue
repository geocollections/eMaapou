<template>
  <search>
    <template #title>
      <title-card
        :title="$t('stratigraphy.pageTitle')"
        icon="mdi-layers-triple"
        class="title-stratigraphy"
      />
    </template>

    <template #form>
      <stratigraphy-search-form />
    </template>

    <template #result>
      <div class="py-1 pl-2 text-h6">
        {{ count ? $tc('common.count', count) : '&nbsp;' }}
      </div>
      <stratigraphy-table
        :show-search="false"
        :items="items"
        :count="count"
        :options="options"
        dynamic-headers
        stateful-headers
        @update="handleUpdate"
      />
    </template>
  </search>
</template>

<script>
import { mapState, mapActions } from 'vuex'
import Search from '@/components/templates/Search'
import StratigraphySearchForm from '@/components/search/forms/StratigraphySearchForm.vue'
import StratigraphyTable from '@/components/tables/StratigraphyTable'
import TitleCard from '~/components/TitleCard.vue'

export default {
  components: {
    Search,
    StratigraphySearchForm,
    StratigraphyTable,
    TitleCard,
  },
  head() {
    return {
      title: this.$t('stratigraphy.pageTitle'),
      meta: [
        {
          property: 'og:title',
          hid: 'og:title',
          content: this.$t('stratigraphy.pageTitle'),
        },
      ],
    }
  },
  computed: {
    ...mapState('search/stratigraphy', ['options', 'items', 'count']),
  },
  methods: {
    ...mapActions('search/stratigraphy', ['searchStratigraphies']),
    async handleUpdate(tableState) {
      await this.searchStratigraphies(tableState?.options)
    },
  },
}
</script>
